// Shared by the RSS feeds and the sitemap — all of them build XML by hand
// rather than pulling in a templating dependency for a handful of tags.
export function escapeXml(value: string): string {
	return value
		.replace(/&/g, '&amp;')
		.replace(/</g, '&lt;')
		.replace(/>/g, '&gt;')
		.replace(/"/g, '&quot;')
		.replace(/'/g, '&apos;');
}
